    <p>With the advent of independence in August 1947, the 563 princely States of India were gradually integrated into different administrative homogeneous units. The State of Rajasthan, in its present form came into being in different stages. The Matsya union comprising Alwar, Bharatpur, Dholpur and Karauli were the first to be ushered in on March 18, 1948. They were joined a week later by Banswara, Bundi, Dungarpur, Jhalawar, Kishangarh, Kushalgarh, Kota, Pratapgarh, Shahpura, Tonk and Udaipur. Exactly a year later, the four larger States viz. Jaipur, Jodhpur, Bikaner and Jaisalmer also joined in. Together they formed the Greater Rajasthan, which was inaugurated by Home Minister of India Sardar Vallabh Bhai Patel on 31st March, 1949. Though the process had started immediately after independence, it was not until 1956 that all the States came together to form the present Rajasthan. The erstwhile princely States constituting Rajasthan varied considerably in size, population, revenue resources, administrative procedures and practices.</p></td>
