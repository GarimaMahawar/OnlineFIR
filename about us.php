  <tr>
    <td colspan="2"> This was duly reflected in the composition and capability of security forces they had maintained for law and order functions. However, with the merger of these States, their police forces were amalgamated into a single police force, which was known as Rajasthan Police. In the intial years after its inception, the Rajasthan Police was headed by officers on deputation and the first Inspector General of Police was Shri R.Banerji, who took over on 7th April, 1949. Shri Banerji held this post for seven months and devoted most of that period to the necessary preliminaries of integration of the various police forces. He chalked out a common police code for the United State of Rajasthan in the Rajasthan Police Regulations. The Rajasthan Police Service was formed in January 1951 and eligible officers from all over the State were appointed. This marked the beginning of Rajasthan Police as we know it today.</td>
